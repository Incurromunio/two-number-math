function Add() {
    var firstAdd = prompt("Enter first number to add");
    var secondAdd = prompt("Enter second number to add");
    var sumAdd = Number(firstAdd) + Number(secondAdd);
    alert(sumAdd);
}


function Subtract() {
    var firstSub = prompt("Enter first number");
    var secondSub = prompt("Enter number to subtract from the first");
    var sumSub = Number(firstSub) - Number(secondSub);
    alert(sumSub);
}

function Multiplication() {
    var firstMul = prompt("Enter first number");
    var secondMul = prompt("Enter second number");
    var sumMul = Number(firstMul) * Number(secondMul);
    alert(sumMul);
}

function Divide() {
    var firstDiv = prompt("Enter first number");
    var secondDiv = prompt("Enter number to divide the first by");
    var sumDiv = Number(firstDiv) / Number(secondDiv);
    alert(sumDiv);
}


